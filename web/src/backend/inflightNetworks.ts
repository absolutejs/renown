// In-flight connectivity detection — the real signal behind the "Mile High Code Club"
// achievement (mile-high). When a /api/submit lands, the server checks the requester's
// egress IP against the announced prefixes of known in-flight Wi-Fi carriers. A hit means
// the traffic is routing through an airplane's satellite/ATG provider — a signal you can't
// forge without actually being on that network (see docs/trust-model.md: this is a
// server-observed signal, not a client self-report).
//
// ── Precision caveat (read before trusting a match as "definitely on a plane") ──
// These are the carriers' FULL announced ranges, which for satellite operators also carry
// their *ground* customers (e.g. Viasat home internet rides the same ASNs as JetBlue
// Fly-Fi). There is no public mapping that isolates the in-flight egress block from the
// home-internet block. So today this is "you're on an in-flight carrier's network"
// granularity, which over-fires for a small set of home satellite-internet users. To
// TIGHTEN to the exact Fly-Fi block: every grant logs the matched IP/CIDR (see
// matchInflightCarrier callers). After a real JetBlue flight we'll see the precise /24 the
// Fly-Fi NAT egresses from and can narrow `viasat` to just those blocks.
//
// ── Backfilling more carriers ──
// To add a carrier, drop its IPv4 prefixes into the `cidrs` array. Pull real data — don't
// guess ranges. The fast way (RIPEstat, no key):
//   curl 'https://stat.ripe.net/data/announced-prefixes/data.json?resource=AS<n>' \
//     | jq -r '.data.prefixes[].prefix' | grep -F .
// In-flight carrier ASNs worth backfilling: Gogo/Intelsat ATG, Panasonic Avionics,
// SITAONAIR, Anuvu/Global Eagle, Thales/LiveTV, Starlink Aviation (AS14593 — but that
// overlaps consumer Starlink, same caveat as Viasat).

